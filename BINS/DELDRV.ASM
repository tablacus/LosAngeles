	LD	A,1
	JR	Z,MAIN
	CALL	#2HEX
	JR	C,USAGE
	CP	7
	JR	NC,USAGE
;
MAIN:
	LD	HL,(#MEMAX)
;
PRO1:
	EX	AF,AF'
	LD	DE,(#MEMAX1)
	LD	A,H
