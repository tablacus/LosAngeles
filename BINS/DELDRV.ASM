;
	LD	A,(HL)
	CP	'['
	JR	NZ,NOTLA
	INC	HL
	LD	B,(HL)
	LD	A,$1F
	CP	B
	JR	NC,PRON
	DEC	HL
	LD	B,5
PRON:
	PUSH	HL
	LD	E,B
	LD	D,0
	ADD	HL,DE
	INC	HL
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
	POP	HL
	INC	HL
PRO2:
	LD	A,(HL)
	INC	HL
	CP	$20
	CALL	NZ,#KYBFS
	DJNZ	PRO2
	LD	A,$20
	CALL	#KYBFS
	LD	A,"/"
	CALL	#KYBFS
	LD	A,"R"
	CALL	#KYBFS
	LD	A,$0D
	CALL	#KYBFS
	LD	A,(HL)
	INC	HL
	LD	H,(HL)
	LD	L,A
	EX	AF,AF'
	DEC	A
	JR	NZ,PRO1
NOTLA:
	AND	A
	RET
