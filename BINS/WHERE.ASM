	JP	C,CDEND
	CALL	#RDFAT
	JP	C,CDEND
;
CDL:
	CALL	POPX
	JP	C,CDEND
	CALL	#PAUSE
	DW	CDEND
	LD	C,(HL)
	INC	HL
	LD	B,(HL)
	INC	HL
	LD	(IY+$1A),C
	LD	(IY+$1B),B
	LD	DE,SEARCH
	LD	BC,126
	LDIR
