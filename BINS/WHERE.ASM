	CP	"/"
	JP	Z,USAGE
	CALL	#FILE
	RET	C
	LD	A,(IX+1)
	CP	$20
	JP	Z,USAGE
;
	LD	HL,(#IBFAD)
	INC	HL
	LD	DE,SFILE
	LD	BC,11
	LDIR
;
	LD	HL,(#MEMAX)
	LD	DE,(#MEMAX1)
	AND	A
	SBC	HL,DE
	ADD	HL,DE
	JR	C,MEM1
	EX	DE,HL
MEM1:
	LD	DE,BUFFA
	XOR	A
	SBC	HL,DE
	ADD	HL,HL
	JR	C,MEM2
	LD	A,H
	DEC	A
MEM2:
	LD	(RING+1),A
;
	CALL	#DEVADR
	LD	A,(#DSK)
	LD	(WILD),A
	LD	L,(IY+$1A)
	LD	H,(IY+$1B)
	LD	(SDIR),HL
	XOR	A
	LD	(STACK),A
	LD	(STOUT),A
