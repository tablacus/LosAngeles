;
	LD	DE,WILD
	CALL	#FILE
	CALL	NC,#SOPENX
CDN:
	JR	C,CDL
	CALL	#PAUSE
	DW	CDEND
;
	PUSH	HL
	POP	IX
	LD	A,(IX)
	CP	"."
	JR	Z,CDNE
;
	LD	A,(IX+$0B)
	BIT	3,A
	JR	NZ,CDNE
	BIT	4,A
	JR	NZ,CDF3
CDF2:
	LD	HL,(#FBAD)
	LD	DE,SFILE
	LD	B,11
	CALL	CPSTR
	JR	NZ,CDNE
;
	CALL	MAKE
	LD	A,(#DSK)
	CALL	#PRINT
	CALL	#MPRNT
	DB	$1A,":",0
	LD	DE,SEARCH
	CALL	#MSX
	LD	A,"\"
	CALL	#PRINT
	LD	DE,FILE
	CALL	#MSX
	CALL	#LTNL
	JR	CDNE
;
CDF3:
	CALL	MAKE
	LD	C,(IX+$1A)
	LD	B,(IX+$1B)
	LD	DE,SEARCH
	CALL	PUSH
	JP	C,CDEND
	LD	(HL),"\"
	LD	DE,FILE
	CALL	PUSH1
CDNE:
	LD	DE,WILD
	CALL	#FILE
	CALL	NC,#NOPENX
	JP	CDN
;
MAKE:
	LD	HL,(#FBAD)
	LD	B,8
	LD	DE,FILE
	CALL	CDF
	LD	A,(HL)
	CP	$21
	JR	C,CDFX
	LD	A,"."
	LD	(DE),A
	LD	B,3
	INC	DE
	CALL	CDF
CDFX:
	XOR	A
	LD	(DE),A
	RET
;
CDF:
	LD	A,(HL)
	INC	HL
	CP	$21
	JR	C,CDFF
	CP	$A0
	JR	Z,CDFF
	LD	(DE),A
	INC	DE
CDFF:
	DJNZ	CDF
	RET
;
CDEND:
	LD	HL,(SDIR)
	LD	(IY+$1A),L
	LD	(IY+$1B),H
	CALL	#NL
	XOR	A
	JP	#MOTOFF
;
PUSH:
	CALL	PUSHX
	RET	C
	LD	(HL),C
	INC	HL
	LD	(HL),B
PUSH1:
	INC	HL
	LD	A,(DE)
	INC	DE
	LD	(HL),A
	OR	A
	JR	NZ,PUSH1
	RET
;
PUSHX:
	LD	A,(STACK)
	LD	HL,(STOUT)
	LD	H,A
	INC	A
	CALL	RING
	CP	L
	SCF
	RET	Z
	LD	(STACK),A
	JR	PUSHX1
POPX:
	LD	A,(STOUT)
	LD	HL,(STACK)
	CP	L
	SCF
	RET	Z
	LD	H,A
	INC	A
	CALL	RING
	LD	(STOUT),A
PUSHX1:
	LD	L,H
	LD	H,0
	ADD	HL,HL
	ADD	HL,HL
	ADD	HL,HL
	ADD	HL,HL
	ADD	HL,HL
	ADD	HL,HL
	ADD	HL,HL
	PUSH	BC
	LD	BC,BUFFA
	ADD	HL,BC
	POP	BC
	RET
;
RING:	CP	$00
	RET	NZ
	XOR	A
	RET
;
