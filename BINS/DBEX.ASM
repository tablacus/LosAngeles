	CP	"-"
	JR	Z,SW2
	JP	DBEX
;
SW2:	INC	DE
	LD	A,(DE)
	CALL	#CAP
	CP	"R"
	JP	Z,REMOVE
	CP	"Z"
	JP	NZ,USAGE
;
DBEX:	CALL	PRO
	RET	C
;
	LD	A,(#GCEH)
	LD	(GCEH),A
	LD	A,(#CLLN)
	LD	(CLLN),A
	LD	HL,(#DTBUF)
	LD	(DTBUF),HL
	LD	HL,(#DIRBF)
	LD	(DIRBF),HL
	LD	HL,(#GCBUF)
	LD	(GCBUF),HL
;
	CALL	#MPRNT
	DB	'MEMAX:',0
	LD	HL,(#MEMAX)
	LD	(START+6),HL
	DEC	H
	LD	A,H
	LD	(#GCEH),A
	LD	A,1
	LD	(#CLLN),A
	DEC	H
	DEC	H
	DEC	H
	LD	L,0
	LD	(#DTBUF),HL
	LD	(#DIRBF),HL
	LD	(#GCBUF),HL
	DEC	H
	LD	L,START-DATA
	LD	(#MEMAX),HL
;
	CALL	#PRTHL
	CALL	#LTNL
;
	EX	DE,HL
	LD	HL,START
	LD	BC,DATA-START
	LDIR
;
	AND	A
	RET
;
