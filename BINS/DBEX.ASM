	CALL	#DEVAD2
	LD	A,(IY)
	CP	3
	JR	NC,REME
	INC	C
	DJNZ	RLOOP1
;
REM2:	CALL	#MPRNT
	DB	'release',$0D,'MEMAX:',0
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
	INC	HL
	LD	(#MEMAX),DE
	CALL	#PRTHL
	CALL	#LTNL
;
	LD	A,(HL)
	INC	HL
	LD	(#GCEH),A
;
	LD	A,(HL)
	INC	HL
	LD	(#CLLN),A
;
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
	INC	HL
	LD	(#DTBUF),DE
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
	INC	HL
	LD	(#DIRBF),DE
	LD	E,(HL)
	INC	HL
	LD	D,(HL)
	LD	(#GCBUF),DE
	AND	A
	RET
;
PRO:	LD	HL,(#MEMAX)
PRO1:	LD	DE,(#MEMAX1)
	AND	A
	EX	DE,HL
	SBC	HL,DE
	EX	DE,HL
	RET	Z
;
	LD	A,(HL)
	CP	'['
	SCF
	CCF
	RET	NZ
	LD	DE,START+1
	INC	HL
	LD	B,(HL)
	INC	HL
	LD	A,$1F
	CP	B
	JR	NC,PRO2
	DEC	HL
	LD	B,5
PRO2:
	PUSH	BC
	PUSH	HL
	CALL	CPSTR
	POP	HL
	POP	BC
	SCF
	RET	Z
	LD	C,B
	LD	B,0
	ADD	HL,BC
	LD	A,(HL)
	INC	HL
	LD	H,(HL)
	LD	L,A
	JR	PRO1
;
CPSTR:	LD	A,(DE)
	CP	(HL)
	RET	NZ
	INC	DE
	INC	HL
	DJNZ	CPSTR
	RET
